package principal;

public class ifelse {

	public static void main(String[] args) {
		
		int manzana, pera, platano;
		
		manzana=2; pera=3; platano=2;
		manzana=23;
		
		if(manzana<=2) {
			System.out.println("solo tengo manzanas");
		}
		if(pera>=4) {
			System.out.println("solo tengo peras");
		}
		if(platano>=7) {
			System.out.println("solo tengo platanos");
		}
		else {
			System.out.println(" no tengo nada ");
		}

	}

}
