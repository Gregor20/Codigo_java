package principal;

public class switch1 {

	public static void main(String[] args) {
		
		int mes=5;
		
		switch (mes) {
		case 1: System.out.println("Enero");
		break;
		
		case 2: System.out.println("Febrero");
		break;
		
		case 3: System.out.println("Marzo");
		break;
		
		default: System.out.println("abril"); //--------- "default": si no es ni 1, ni 2, ni 3, por defecto ser√° "abril cualquier otro numero.
		}
			
		
		

	}

}
