public class ejercicio_3_do_while {
	
	//muestra los numeros multiplos de 5, ordenados de 0 a 100, utilizando un bucle "do-while"

	public static void main(String[] args) {
		
		int i = 0;
		do {
			
		System.out.printf("%1d ", i);
		i+=5;
		
		
		}
		 while (i<=100);
		

	}

}
